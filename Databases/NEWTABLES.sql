CREATE TABLE REGISTRATION(
ID nvarchar(50) primary key,
NAME NVARCHAR(20) NOT NULL,
SURNAME NVARCHAR(20) NOT NULL,
LOGIN NVARCHAR(12) NOT NULL,
PASSWORD NVARCHAR(15) NOT NULL
);

